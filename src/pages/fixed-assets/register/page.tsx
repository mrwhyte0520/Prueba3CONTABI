import { useState, useEffect } from 'react';
import { useNavigate } from 'react-router-dom';
import DashboardLayout from '../../../components/layout/DashboardLayout';
import { useAuth } from '../../../hooks/useAuth';
import { exportToExcelWithHeaders } from '../../../utils/exportImportUtils';
import { fixedAssetsService, assetTypesService, suppliersService, settingsService } from '../../../services/database';
import { formatMoney } from '../../../utils/numberFormat';

interface Asset {
  id: string;
  code: string;
  name: string;
  category: string;
  location: string;
  acquisitionDate: string;
  acquisitionCost: number;
  usefulLife: number;
  depreciationMethod: string;
  currentValue: number;
  accumulatedDepreciation: number;
  status: string;
  supplier: string;
  description: string;
}

export default function AssetRegisterPage() {
  const navigate = useNavigate();
  const { user } = useAuth();
  const [showModal, setShowModal] = useState(false);
  const [editingAsset, setEditingAsset] = useState<Asset | null>(null);
  const [searchTerm, setSearchTerm] = useState('');
  const [filterCategory, setFilterCategory] = useState('');
  const [filterStatus, setFilterStatus] = useState('');

  const [assets, setAssets] = useState<Asset[]>([]);
  const [categories, setCategories] = useState<string[]>([]);
  const [suppliers, setSuppliers] = useState<any[]>([]);
  const [assetTypes, setAssetTypes] = useState<any[]>([]);
  const [codeValue, setCodeValue] = useState('');
  const [formCategory, setFormCategory] = useState('');
  const [usefulLifeValue, setUsefulLifeValue] = useState('');
  const [depreciationMethodValue, setDepreciationMethodValue] = useState('');

  useEffect(() => {
    const loadData = async () => {
      if (!user) return;
      try {
        const [assetsData, typesData, suppliersData] = await Promise.all([
          fixedAssetsService.getAll(user.id),
          assetTypesService.getAll(user.id),
          suppliersService.getAll(user.id),
        ]);

        const mappedAssets: Asset[] = (assetsData || []).map((a: any) => ({
          id: a.id,
          code: a.code,
          name: a.name,
          category: a.category,
          location: a.location || '',
          acquisitionDate: a.purchase_date,
          acquisitionCost: Number(a.purchase_cost) || 0,
          usefulLife: a.useful_life,
          depreciationMethod: a.depreciation_method,
          currentValue: Number(a.current_value) || 0,
          accumulatedDepreciation: Number(a.accumulated_depreciation) || 0,
          status: a.status,
          supplier: a.supplier || '',
          description: a.description || '',
        }));
        setAssets(mappedAssets);

        const activeTypes = (typesData || []).filter((t: any) => t.is_active !== false);
        const mappedCategories = activeTypes.map((t: any) => String(t.name || '')).filter(Boolean);
        setAssetTypes(activeTypes || []);
        setCategories(mappedCategories);

        setSuppliers(Array.isArray(suppliersData) ? suppliersData : []);
      } catch (error) {
        console.error('Error loading fixed assets data:', error);
      }
    };

    loadData();
  }, [user]);

  const depreciationMethods = [
    'Línea Recta',
    'Saldo Decreciente',
    'Suma de Dígitos',
    'Unidades de Producción'
  ];

  const filteredAssets = assets.filter(asset => {
    const matchesSearch = asset.name.toLowerCase().includes(searchTerm.toLowerCase()) ||
                         asset.code.toLowerCase().includes(searchTerm.toLowerCase());
    const matchesCategory = !filterCategory || asset.category === filterCategory;
    const matchesStatus = !filterStatus || asset.status === filterStatus;
    
    return matchesSearch && matchesCategory && matchesStatus;
  });

  const handleAddAsset = () => {
    setEditingAsset(null);
    setCodeValue('');
    setFormCategory('');
    setUsefulLifeValue('');
    setDepreciationMethodValue('');
    setShowModal(true);
  };

  const handleEditAsset = (asset: Asset) => {
    setEditingAsset(asset);
    setCodeValue(asset.code);
    setFormCategory(asset.category || '');
    setUsefulLifeValue(asset.usefulLife != null ? String(asset.usefulLife) : '');
    setDepreciationMethodValue(asset.depreciationMethod || '');
    setShowModal(true);
  };

  const handleDeleteAsset = async (assetId: string) => {
    if (!confirm('¿Está seguro de que desea eliminar este activo?')) return;
    try {
      await fixedAssetsService.delete(assetId);
      setAssets(prev => prev.filter(asset => asset.id !== assetId));
    } catch (error) {
      console.error('Error deleting asset:', error);
      alert('Error al eliminar el activo');
    }
  };

  const handleGenerateCode = () => {
    // Generar un código simple basado en la cantidad existente, ej: ACT-001, ACT-002, etc.
    const base = 'ACT';
    const nextNumber = assets.length + 1;
    const padded = String(nextNumber).padStart(3, '0');
    setCodeValue(`${base}-${padded}`);
  };

  const handleCategoryChange = (value: string) => {
    setFormCategory(value);
    if (!value) return;
    const type = assetTypes.find((t: any) => String(t.name || '') === value);
    if (type) {
      if (type.useful_life != null) {
        setUsefulLifeValue(String(type.useful_life));
      }
      if (type.depreciation_method) {
        setDepreciationMethodValue(String(type.depreciation_method));
      }
    }
  };

  const handleSaveAsset = async (e: React.FormEvent<HTMLFormElement>) => {
    e.preventDefault();
    if (!user) return;

    const formData = new FormData(e.currentTarget);
    const payload: any = {
      code: String(formData.get('code') || '').trim() || codeValue.trim(),
      name: String(formData.get('name') || '').trim(),
      category: String(formData.get('category') || formCategory || '').trim(),
      location: String(formData.get('location') || '').trim() || null,
      purchase_date: String(formData.get('acquisitionDate') || ''),
      purchase_cost: Number(formData.get('acquisitionCost') || 0) || 0,
      useful_life: Number(usefulLifeValue || formData.get('usefulLife') || 0) || 0,
      depreciation_method: String(depreciationMethodValue || formData.get('depreciationMethod') || '').trim(),
      current_value: Number(formData.get('currentValue') || 0) || 0,
      accumulated_depreciation: Number(formData.get('accumulatedDepreciation') || 0) || 0,
      status: String(formData.get('status') || 'Activo'),
      supplier: String(formData.get('supplier') || '').trim() || null,
      description: String(formData.get('description') || '').trim() || null,
    };

    try {
      if (editingAsset) {
        const updated = await fixedAssetsService.update(editingAsset.id, payload);
        const mapped: Asset = {
          id: updated.id,
          code: updated.code,
          name: updated.name,
          category: updated.category,
          location: updated.location || '',
          acquisitionDate: updated.purchase_date,
          acquisitionCost: Number(updated.purchase_cost) || 0,
          usefulLife: updated.useful_life,
          depreciationMethod: updated.depreciation_method,
          currentValue: Number(updated.current_value) || 0,
          accumulatedDepreciation: Number(updated.accumulated_depreciation) || 0,
          status: updated.status,
          supplier: updated.supplier || '',
          description: updated.description || '',
        };
        setAssets(prev => prev.map(asset => asset.id === editingAsset.id ? mapped : asset));
      } else {
        const created = await fixedAssetsService.create(user.id, payload);
        const mapped: Asset = {
          id: created.id,
          code: created.code,
          name: created.name,
          category: created.category,
          location: created.location || '',
          acquisitionDate: created.purchase_date,
          acquisitionCost: Number(created.purchase_cost) || 0,
          usefulLife: created.useful_life,
          depreciationMethod: created.depreciation_method,
          currentValue: Number(created.current_value) || 0,
          accumulatedDepreciation: Number(created.accumulated_depreciation) || 0,
          status: created.status,
          supplier: created.supplier || '',
          description: created.description || '',
        };
        setAssets(prev => [...prev, mapped]);
      }

      setShowModal(false);
      setEditingAsset(null);
    } catch (error) {
      console.error('Error saving asset:', error);
      alert('Error al guardar el activo');
    }
  };

  const exportToExcel = async () => {
    const filteredData = filteredAssets;
    if (!filteredData || filteredData.length === 0) {
      alert('No hay datos para exportar.');
      return;
    }

    let companyName = 'ContaBi';
    try {
      const info = await settingsService.getCompanyInfo();
      if (info && (info as any)) {
        const resolvedName =
          (info as any).name ||
          (info as any).company_name ||
          (info as any).legal_name;
        if (resolvedName) {
          companyName = String(resolvedName);
        }
      }
    } catch (error) {
      console.error('Error obteniendo información de la empresa para Excel de activos fijos:', error);
    }

    const rows = filteredData.map((asset) => ({
      code: asset.code,
      name: asset.name,
      category: asset.category,
      location: asset.location,
      acquisition_date: asset.acquisitionDate,
      acquisition_cost: asset.acquisitionCost,
      useful_life: asset.usefulLife,
      depreciation_method: asset.depreciationMethod,
      current_value: asset.currentValue,
      accumulated_depreciation: asset.accumulatedDepreciation,
      status: asset.status,
      supplier: asset.supplier,
      description: asset.description,
    }));

    const headers = [
      { key: 'code', title: 'Código' },
      { key: 'name', title: 'Nombre del Activo' },
      { key: 'category', title: 'Categoría' },
      { key: 'location', title: 'Ubicación' },
      { key: 'acquisition_date', title: 'Fecha Adquisición' },
      { key: 'acquisition_cost', title: 'Costo Adquisición' },
      { key: 'useful_life', title: 'Vida Útil (años)' },
      { key: 'depreciation_method', title: 'Método Depreciación' },
      { key: 'current_value', title: 'Valor Actual' },
      { key: 'accumulated_depreciation', title: 'Depreciación Acumulada' },
      { key: 'status', title: 'Estado' },
      { key: 'supplier', title: 'Proveedor' },
      { key: 'description', title: 'Descripción' },
    ];

    const today = new Date().toISOString().split('T')[0];
    const fileBase = `registro_activos_${today}`;
    const title = 'Registro de Activos Fijos';

    exportToExcelWithHeaders(
      rows,
      headers,
      fileBase,
      'Activos',
      [12, 28, 20, 18, 16, 16, 14, 20, 16, 20, 14, 20, 32],
      {
        title,
        companyName,
      },
    );
  };

  const formatCurrency = (amount: number) => {
    return formatMoney(amount, 'RD$');
  };

  return (
    <DashboardLayout>
      <div className="space-y-6">
        {/* Header */}
        <div className="flex items-center justify-between">
          <div>
            <button
              onClick={() => navigate('/fixed-assets')}
              className="flex items-center text-blue-600 hover:text-blue-700 mb-2"
            >
              <i className="ri-arrow-left-line mr-1"></i>
              Volver a Activos Fijos
            </button>
            <h1 className="text-2xl font-bold text-gray-900">Registro de Activos</h1>
            <p className="text-gray-600">Gestión y mantenimiento de activos fijos</p>
          </div>
          <div className="flex space-x-3">
            <button
              onClick={exportToExcel}
              className="bg-green-600 text-white px-4 py-2 rounded-lg hover:bg-green-700 transition-colors whitespace-nowrap"
            >
              <i className="ri-file-excel-line mr-2"></i>
              Exportar Excel
            </button>
            <button
              onClick={handleAddAsset}
              className="bg-blue-600 text-white px-4 py-2 rounded-lg hover:bg-blue-700 transition-colors whitespace-nowrap"
            >
              <i className="ri-add-line mr-2"></i>
              Nuevo Activo
            </button>
          </div>
        </div>

        {/* Filters */}
        <div className="bg-white rounded-lg shadow-sm border border-gray-200 p-6">
          <div className="grid grid-cols-1 md:grid-cols-4 gap-4">
            <div>
              <label className="block text-sm font-medium text-gray-700 mb-2">
                Buscar
              </label>
              <div className="relative">
                <i className="ri-search-line absolute left-3 top-1/2 transform -translate-y-1/2 text-gray-400"></i>
                <input
                  type="text"
                  placeholder="Buscar por nombre o código..."
                  value={searchTerm}
                  onChange={(e) => setSearchTerm(e.target.value)}
                  className="w-full pl-10 pr-4 py-2 border border-gray-300 rounded-lg focus:ring-2 focus:ring-blue-500 focus:border-blue-500"
                />
              </div>
            </div>
            <div>
              <label className="block text-sm font-medium text-gray-700 mb-2">
                Categoría
              </label>
              <select
                value={filterCategory}
                onChange={(e) => setFilterCategory(e.target.value)}
                className="w-full px-3 py-2 border border-gray-300 rounded-lg focus:ring-2 focus:ring-blue-500 focus:border-blue-500"
              >
                <option value="">Todas las categorías</option>
                {categories.map(category => (
                  <option key={category} value={category}>{category}</option>
                ))}
              </select>
            </div>
            <div>
              <label className="block text-sm font-medium text-gray-700 mb-2">
                Estado
              </label>
              <select
                value={filterStatus}
                onChange={(e) => setFilterStatus(e.target.value)}
                className="w-full px-3 py-2 border border-gray-300 rounded-lg focus:ring-2 focus:ring-blue-500 focus:border-blue-500"
              >
                <option value="">Todos los estados</option>
                <option value="Activo">Activo</option>
                <option value="Inactivo">Inactivo</option>
                <option value="En Mantenimiento">En Mantenimiento</option>
                <option value="Dado de Baja">Dado de Baja</option>
              </select>
            </div>
            <div className="flex items-end">
              <button
                onClick={() => {
                  setSearchTerm('');
                  setFilterCategory('');
                  setFilterStatus('');
                }}
                className="w-full bg-gray-500 text-white px-4 py-2 rounded-lg hover:bg-gray-600 transition-colors whitespace-nowrap"
              >
                Limpiar Filtros
              </button>
            </div>
          </div>
        </div>

        {/* Assets Table */}
        <div className="bg-white rounded-lg shadow-sm border border-gray-200">
          <div className="p-6 border-b border-gray-200">
            <h3 className="text-lg font-semibold text-gray-900">
              Activos Registrados ({filteredAssets.length})
            </h3>
          </div>
          <div className="overflow-x-auto">
            <table className="w-full">
              <thead className="bg-gray-50">
                <tr>
                  <th className="px-6 py-3 text-left text-xs font-medium text-gray-500 uppercase tracking-wider">
                    Código
                  </th>
                  <th className="px-6 py-3 text-left text-xs font-medium text-gray-500 uppercase tracking-wider">
                    Activo
                  </th>
                  <th className="px-6 py-3 text-left text-xs font-medium text-gray-500 uppercase tracking-wider">
                    Categoría
                  </th>
                  <th className="px-6 py-3 text-left text-xs font-medium text-gray-500 uppercase tracking-wider">
                    Costo Adquisición
                  </th>
                  <th className="px-6 py-3 text-left text-xs font-medium text-gray-500 uppercase tracking-wider">
                    Valor Actual
                  </th>
                  <th className="px-6 py-3 text-left text-xs font-medium text-gray-500 uppercase tracking-wider">
                    Estado
                  </th>
                  <th className="px-6 py-3 text-left text-xs font-medium text-gray-500 uppercase tracking-wider">
                    Acciones
                  </th>
                </tr>
              </thead>
              <tbody className="bg-white divide-y divide-gray-200">
                {filteredAssets.map((asset) => (
                  <tr key={asset.id} className="hover:bg-gray-50">
                    <td className="px-6 py-4 whitespace-nowrap text-sm font-medium text-gray-900">
                      {asset.code}
                    </td>
                    <td className="px-6 py-4 whitespace-nowrap">
                      <div>
                        <div className="text-sm font-medium text-gray-900">{asset.name}</div>
                        <div className="text-sm text-gray-500">{asset.location}</div>
                      </div>
                    </td>
                    <td className="px-6 py-4 whitespace-nowrap text-sm text-gray-900">
                      {asset.category}
                    </td>
                    <td className="px-6 py-4 whitespace-nowrap text-sm text-gray-900">
                      {formatCurrency(asset.acquisitionCost)}
                    </td>
                    <td className="px-6 py-4 whitespace-nowrap text-sm text-gray-900">
                      {formatCurrency(asset.currentValue)}
                    </td>
                    <td className="px-6 py-4 whitespace-nowrap">
                      <span
                        className={`inline-flex px-2 py-1 text-xs font-semibold rounded-full ${
                          asset.status === 'active' || asset.status === 'Activo'
                            ? 'bg-green-100 text-green-800'
                            : asset.status === 'disposed' || asset.status === 'Dado de Baja'
                            ? 'bg-yellow-100 text-yellow-800'
                            : asset.status === 'sold' || asset.status === 'Vendido'
                            ? 'bg-blue-100 text-blue-800'
                            : 'bg-gray-100 text-gray-800'
                        }`}
                      >
                        {asset.status === 'active' || asset.status === 'Activo'
                          ? 'Activo'
                          : asset.status === 'disposed' || asset.status === 'Dado de Baja'
                          ? 'Retirado'
                          : asset.status === 'sold' || asset.status === 'Vendido'
                          ? 'Vendido'
                          : asset.status}
                      </span>
                    </td>
                    <td className="px-6 py-4 whitespace-nowrap text-sm font-medium">
                      <div className="flex space-x-2">
                        <button
                          onClick={() => handleEditAsset(asset)}
                          className="text-blue-600 hover:text-blue-900"
                        >
                          <i className="ri-edit-line"></i>
                        </button>
                        <button
                          onClick={() => handleDeleteAsset(asset.id)}
                          className="text-red-600 hover:text-red-900"
                        >
                          <i className="ri-delete-bin-line"></i>
                        </button>
                      </div>
                    </td>
                  </tr>
                ))}
              </tbody>
            </table>
          </div>
        </div>

        {/* Asset Modal */}
        {showModal && (
          <div className="fixed inset-0 bg-black bg-opacity-50 flex items-center justify-center z-50">
            <div className="bg-white rounded-lg p-6 w-full max-w-4xl max-h-[90vh] overflow-y-auto">
              <div className="flex items-center justify-between mb-6">
                <h3 className="text-lg font-semibold text-gray-900">
                  {editingAsset ? 'Editar Activo' : 'Nuevo Activo'}
                </h3>
                <button
                  onClick={() => setShowModal(false)}
                  className="text-gray-400 hover:text-gray-600"
                >
                  <i className="ri-close-line text-xl"></i>
                </button>
              </div>

              <form onSubmit={handleSaveAsset} className="space-y-6">
                <div className="grid grid-cols-1 md:grid-cols-2 gap-6">
                  <div>
                    <label className="block text-sm font-medium text-gray-700 mb-2">
                      Código del Activo *
                    </label>
                    <div className="flex gap-2">
                      <input
                        type="text"
                        required
                        name="code"
                        value={codeValue}
                        onChange={(e) => setCodeValue(e.target.value)}
                        className="flex-1 px-3 py-2 border border-gray-300 rounded-lg focus:ring-2 focus:ring-blue-500 focus:border-blue-500"
                        placeholder="ACT-001"
                      />
                      <button
                        type="button"
                        onClick={handleGenerateCode}
                        className="px-3 py-2 bg-gray-100 text-gray-700 rounded-lg border border-gray-300 hover:bg-gray-200 transition-colors whitespace-nowrap text-sm"
                      >
                        Generar
                      </button>
                    </div>
                  </div>
                  <div>
                    <label className="block text-sm font-medium text-gray-700 mb-2">
                      Nombre del Activo *
                    </label>
                    <input
                      type="text"
                      required
                      name="name"
                      defaultValue={editingAsset?.name || ''}
                      className="w-full px-3 py-2 border border-gray-300 rounded-lg focus:ring-2 focus:ring-blue-500 focus:border-blue-500"
                      placeholder="Nombre del activo"
                    />
                  </div>
                  <div>
                    <label className="block text-sm font-medium text-gray-700 mb-2">
                      Tipo de Activo *
                    </label>
                    <select
                      required
                      name="category"
                      value={formCategory}
                      onChange={(e) => handleCategoryChange(e.target.value)}
                      className="w-full px-3 py-2 border border-gray-300 rounded-lg focus:ring-2 focus:ring-blue-500 focus:border-blue-500"
                    >
                      <option value="">Seleccionar categoría</option>
                      {categories.map(category => (
                        <option key={category} value={category}>{category}</option>
                      ))}
                    </select>
                  </div>
                  <div>
                    <label className="block text-sm font-medium text-gray-700 mb-2">
                      Ubicación
                    </label>
                    <input
                      type="text"
                      name="location"
                      defaultValue={editingAsset?.location || ''}
                      className="w-full px-3 py-2 border border-gray-300 rounded-lg focus:ring-2 focus:ring-blue-500 focus:border-blue-500"
                      placeholder="Ubicación del activo"
                    />
                  </div>
                  <div>
                    <label className="block text-sm font-medium text-gray-700 mb-2">
                      Fecha de Adquisición *
                    </label>
                    <input
                      type="date"
                      required
                      name="acquisitionDate"
                      defaultValue={editingAsset?.acquisitionDate || ''}
                      className="w-full px-3 py-2 border border-gray-300 rounded-lg focus:ring-2 focus:ring-blue-500 focus:border-blue-500"
                    />
                  </div>
                  <div>
                    <label className="block text-sm font-medium text-gray-700 mb-2">
                      Costo de Adquisición *
                    </label>
                    <input
                      type="number" min="0"
                      required
                      step="0.01"
                      name="acquisitionCost"
                      defaultValue={editingAsset?.acquisitionCost || ''}
                      className="w-full px-3 py-2 border border-gray-300 rounded-lg focus:ring-2 focus:ring-blue-500 focus:border-blue-500"
                      placeholder="0.00"
                    />
                  </div>
                  <div>
                    <label className="block text-sm font-medium text-gray-700 mb-2">
                      Vida Útil (años) *
                    </label>
                    <input
                      type="number" min="0"
                      required
                      name="usefulLife"
                      value={usefulLifeValue}
                      onChange={(e) => setUsefulLifeValue(e.target.value)}
                      className="w-full px-3 py-2 border border-gray-300 rounded-lg focus:ring-2 focus:ring-blue-500 focus:border-blue-500"
                      placeholder="10"
                    />
                  </div>
                  <div>
                    <label className="block text-sm font-medium text-gray-700 mb-2">
                      Método de Depreciación *
                    </label>
                    <select
                      required
                      name="depreciationMethod"
                      value={depreciationMethodValue}
                      onChange={(e) => setDepreciationMethodValue(e.target.value)}
                      className="w-full px-3 py-2 border border-gray-300 rounded-lg focus:ring-2 focus:ring-blue-500 focus:border-blue-500"
                    >
                      <option value="">Seleccionar método</option>
                      {depreciationMethods.map(method => (
                        <option key={method} value={method}>{method}</option>
                      ))}
                    </select>
                  </div>
                  <div>
                    <label className="block text-sm font-medium text-gray-700 mb-2">
                      Proveedor
                    </label>
                    <select
                      name="supplier"
                      defaultValue={editingAsset?.supplier || ''}
                      className="w-full px-3 py-2 border border-gray-300 rounded-lg focus:ring-2 focus:ring-blue-500 focus:border-blue-500"
                    >
                      <option value="">Seleccionar proveedor</option>
                      {suppliers.map((s: any) => (
                        <option key={s.id} value={s.name || s.company_name || ''}>
                          {s.name || s.company_name || 'Proveedor'}
                        </option>
                      ))}
                    </select>
                  </div>
                  <div>
                    <label className="block text-sm font-medium text-gray-700 mb-2">
                      Estado
                    </label>
                    <select
                      name="status"
                      defaultValue={editingAsset?.status || 'active'}
                      className="w-full px-3 py-2 border border-gray-300 rounded-lg focus:ring-2 focus:ring-blue-500 focus:border-blue-500"
                    >
                      <option value="active">Activo</option>
                      <option value="disposed">Retirado</option>
                      <option value="sold">Vendido</option>
                    </select>
                  </div>
                </div>
                <div>
                  <label className="block text-sm font-medium text-gray-700 mb-2">
                    Descripción
                  </label>
                  <textarea
                    rows={3}
                    name="description"
                    defaultValue={editingAsset?.description || ''}
                    className="w-full px-3 py-2 border border-gray-300 rounded-lg focus:ring-2 focus:ring-blue-500 focus:border-blue-500"
                    placeholder="Descripción detallada del activo"
                  />
                </div>

                <div className="flex justify-end space-x-3">
                  <button
                    type="button"
                    onClick={() => setShowModal(false)}
                    className="px-4 py-2 text-gray-700 bg-gray-200 rounded-lg hover:bg-gray-300 transition-colors whitespace-nowrap"
                  >
                    Cancelar
                  </button>
                  <button
                    type="submit"
                    className="px-4 py-2 bg-blue-600 text-white rounded-lg hover:bg-blue-700 transition-colors whitespace-nowrap"
                  >
                    {editingAsset ? 'Actualizar' : 'Registrar'} Activo
                  </button>
                </div>
              </form>
            </div>
          </div>
        )}
      </div>
    </DashboardLayout>
  );
}