import { useState, useEffect } from 'react';
import { DashboardLayout } from '../../../components/layout/DashboardLayout';
import { taxService, settingsService } from '../../../services/database';
import * as XLSX from 'xlsx';
import { exportToPdf } from '../../../utils/exportImportUtils';
import { formatDateEsDO } from '../../../utils/date';
import { formatAmount } from '../../../utils/numberFormat';

interface Report606Data {
  id: string;
  period: string;
  rnc_cedula: string;
  tipo_identificacion: string;
  tipo_bienes_servicios: string;
  ncf: string;
  ncf_modificado?: string;
  fecha_comprobante: string;
  fecha_pago: string;
  servicios_facturados: number;
  bienes_facturados: number;
  monto_facturado: number;
  itbis_facturado: number;
  itbis_retenido: number;
  retencion_renta: number;
  isr_percibido: number;
  impuesto_selectivo_consumo: number;
  otros_impuestos: number;
  monto_propina_legal: number;
  forma_pago: string;
}

interface Report606Summary {
  totalRecords: number;
  totalAmount: number;
  totalItbis: number;
  totalRetention: number;
}

export default function Report606Page() {
  const [selectedPeriod, setSelectedPeriod] = useState('');
  const [selectedYear, setSelectedYear] = useState<string>(new Date().getFullYear().toString());
  const [selectedMonth, setSelectedMonth] = useState<string>(String(new Date().getMonth() + 1).padStart(2, '0'));
  const [reportData, setReportData] = useState<Report606Data[]>([]);
  const [summary, setSummary] = useState<Report606Summary | null>(null);
  const [loading, setLoading] = useState(false);
  const [showResults, setShowResults] = useState(false);
  const [companyInfo, setCompanyInfo] = useState<any | null>(null);

  const months = [
    { value: '01', label: 'Enero' },
    { value: '02', label: 'Febrero' },
    { value: '03', label: 'Marzo' },
    { value: '04', label: 'Abril' },
    { value: '05', label: 'Mayo' },
    { value: '06', label: 'Junio' },
    { value: '07', label: 'Julio' },
    { value: '08', label: 'Agosto' },
    { value: '09', label: 'Septiembre' },
    { value: '10', label: 'Octubre' },
    { value: '11', label: 'Noviembre' },
    { value: '12', label: 'Diciembre' },
  ];

  const currentYear = new Date().getFullYear();
  const years = Array.from({ length: 5 }, (_, idx) => String(currentYear - idx));

  useEffect(() => {
    const loadCompany = async () => {
      try {
        const info = await settingsService.getCompanyInfo();
        setCompanyInfo(info);
      } catch (error) {
        console.error('Error cargando información de la empresa para Reporte 606', error);
      }
    };

    loadCompany();
  }, []);

  const generateReport = async () => {
    const period = `${selectedYear}-${selectedMonth}`;
    setSelectedPeriod(period);

    if (!selectedYear || !selectedMonth) {
      alert('Por favor selecciona mes y año');
      return;
    }

    setLoading(true);
    try {
      // Construir los datos del 606 para el período antes de consultar el reporte
      await taxService.buildReport606(period);

      const data = await taxService.generateReport606(period);
      const summaryData = await taxService.getReport606Summary(period) as any;

      const missingExpenseTypeCount = Array.isArray(data)
        ? data.filter((row: any) => !row?.tipo_bienes_servicios || String(row.tipo_bienes_servicios).trim() === '').length
        : 0;

      if (missingExpenseTypeCount > 0) {
        const proceed = confirm(
          `Hay ${missingExpenseTypeCount} registro(s) sin "Tipo de gasto (606)". ` +
          'Esto puede afectar el archivo oficial para la DGII. ¿Deseas continuar de todos modos?'
        );
        if (!proceed) {
          setLoading(false);
          return;
        }
      }

      const totalRecords = Array.isArray(data) ? data.length : 0;
      const totalAmount = Number(summaryData?.totalMonto ?? 0);
      const totalItbis = Number(summaryData?.totalItbis ?? 0);
      const totalRetention = Number(summaryData?.totalRetenido ?? 0);

      setReportData(Array.isArray(data) ? data : []);
      setSummary({ totalRecords, totalAmount, totalItbis, totalRetention });
      setShowResults(true);
    } catch (error) {
      console.error('Error generating report:', error);
      alert('Error al generar el reporte');
    } finally {
      setLoading(false);
    }
  };

  const exportToCSV = () => {
    if (reportData.length === 0) return;

    const separator = ';';

    const headers = [
      'RNC/Cédula',
      'Tipo ID',
      'Tipo Bienes/Servicios',
      'NCF',
      'NCF Modificado',
      'Fecha Comprobante',
      'Fecha Pago',
      'Servicios Facturados',
      'Bienes Facturados',
      'Monto Facturado',
      'ITBIS Facturado',
      'ITBIS Retenido',
      'Retención Renta',
      'ISR Percibido',
      'Impuesto Selectivo',
      'Otros Impuestos',
      'Propina Legal',
      'Forma Pago'
    ];

    const companyName =
      (companyInfo as any)?.name ||
      (companyInfo as any)?.company_name ||
      'ContaBi';

    const companyRnc =
      (companyInfo as any)?.rnc ||
      (companyInfo as any)?.tax_id ||
      '';

    const headerLines: string[] = [
      ['Empresa', companyName].join(separator),
    ];

    if (companyRnc) {
      headerLines.push(['RNC', companyRnc].join(separator));
    }

    headerLines.push(['Reporte', 'Reporte 606 - Compras y Servicios'].join(separator));
    headerLines.push(['Período', selectedPeriod].join(separator));
    headerLines.push('');

    const csvContent = [
      ...headerLines,
      headers.join(separator),
      ...reportData.map(row => [
        row.rnc_cedula,
        row.tipo_identificacion,
        row.tipo_bienes_servicios,
        row.ncf,
        row.ncf_modificado || '',
        row.fecha_comprobante,
        row.fecha_pago,
        Number(row.servicios_facturados ?? 0).toFixed(2),
        Number(row.bienes_facturados ?? 0).toFixed(2),
        Number(row.monto_facturado ?? 0).toFixed(2),
        Number(row.itbis_facturado ?? 0).toFixed(2),
        Number(row.itbis_retenido ?? 0).toFixed(2),
        Number(row.retencion_renta ?? 0).toFixed(2),
        Number(row.isr_percibido ?? 0).toFixed(2),
        Number(row.impuesto_selectivo_consumo ?? 0).toFixed(2),
        Number(row.otros_impuestos ?? 0).toFixed(2),
        Number(row.monto_propina_legal ?? 0).toFixed(2),
        row.forma_pago
      ].join(separator))
    ].join('\n');

    const csvForExcel = '\uFEFF' + csvContent.replace(/\n/g, '\r\n');
    const blob = new Blob([csvForExcel], { type: 'text/csv;charset=utf-8;' });
    const link = document.createElement('a');
    link.href = URL.createObjectURL(blob);
    link.download = `reporte_606_${selectedPeriod}.csv`;
    link.click();
  };

  const exportToExcel = () => {
    if (reportData.length === 0) return;

    // Preparar datos para Excel
    const excelData = reportData.map(row => ({
      'RNC/Cédula': row.rnc_cedula,
      'Tipo ID': row.tipo_identificacion,
      'Tipo Bien/Serv.': row.tipo_bienes_servicios,
      'NCF': row.ncf,
      'NCF Mod.': row.ncf_modificado || '',
      'F. Comp.': row.fecha_comprobante,
      'F. Pago': row.fecha_pago,
      'Serv. Fact.': row.servicios_facturados,
      'Bienes Fact.': row.bienes_facturados,
      'Monto Fact.': row.monto_facturado,
      'ITBIS Fact.': row.itbis_facturado,
      'ITBIS Ret.': row.itbis_retenido,
      'Ret. Renta': row.retencion_renta,
      'Forma Pago': row.forma_pago
    }));

    const companyName =
      (companyInfo as any)?.name ||
      (companyInfo as any)?.company_name ||
      'ContaBi';

    const companyRnc =
      (companyInfo as any)?.rnc ||
      (companyInfo as any)?.tax_id ||
      '';

    const headerRows: (string | number)[][] = [];

    headerRows.push([companyName]);
    if (companyRnc) {
      headerRows.push([`RNC: ${companyRnc}`]);
    }
    headerRows.push(['Reporte 606 - Compras y Servicios']);
    headerRows.push([`Período: ${selectedPeriod}`]);
    headerRows.push([]);

    // Crear libro de trabajo
    const wb = XLSX.utils.book_new();

    const tableStartRow = headerRows.length + 1;
    const ws = XLSX.utils.json_to_sheet(excelData as any, { origin: `A${tableStartRow}` } as any);

    XLSX.utils.sheet_add_aoa(ws, headerRows, { origin: 'A1' });

    // Centrar y resaltar encabezado (empresa, nombre del reporte y período)
    const totalColumns = Object.keys(excelData[0] || {}).length || 1;
    const merges: any[] = (ws as any)['!merges'] || [];

    // Fusionar las primeras filas de encabezado sobre todas las columnas de datos
    for (let r = 0; r < headerRows.length - 1; r++) {
      merges.push({
        s: { r, c: 0 },
        e: { r, c: totalColumns - 1 },
      });
    }
    (ws as any)['!merges'] = merges;

    // Aplicar estilos: centrado y fuente más grande para el título del reporte
    for (let r = 0; r < headerRows.length - 1; r++) {
      const cellRef = `A${r + 1}`;
      const cell = (ws as any)[cellRef];
      if (!cell) continue;

      const existingStyle = (cell as any).s || {};
      const font: any = {
        ...(existingStyle.font || {}),
        bold: true,
      };

      if (typeof cell.v === 'string' && cell.v.includes('Reporte 606')) {
        font.sz = 16; // Título principal del reporte
      } else if (r === 0) {
        font.sz = 14; // Nombre de la empresa
      } else {
        font.sz = 12; // Otras líneas de encabezado (RNC, período)
      }

      (cell as any).s = {
        ...existingStyle,
        alignment: {
          ...(existingStyle.alignment || {}),
          horizontal: 'center',
          vertical: 'center',
        },
        font,
      };
    }

    // Configurar ancho de columnas (alineado con las columnas simplificadas)
    const colWidths = [
      { wch: 15 }, // RNC/Cédula
      { wch: 10 }, // Tipo ID
      { wch: 20 }, // Tipo Bien/Serv.
      { wch: 15 }, // NCF
      { wch: 12 }, // NCF Mod.
      { wch: 12 }, // F. Comp.
      { wch: 12 }, // F. Pago
      { wch: 14 }, // Serv. Fact.
      { wch: 14 }, // Bienes Fact.
      { wch: 14 }, // Monto Fact.
      { wch: 14 }, // ITBIS Fact.
      { wch: 14 }, // ITBIS Ret.
      { wch: 14 }, // Ret. Renta
      { wch: 12 }  // Forma Pago
    ];
    ws['!cols'] = colWidths;

    // Agregar hoja al libro
    XLSX.utils.book_append_sheet(wb, ws, 'Reporte 606');

    // Si hay resumen, agregar hoja de resumen
    if (summary) {
      const summaryData = [
        { 'Concepto': 'Total de Registros', 'Valor': summary.totalRecords },
        { 'Concepto': 'Monto Total Facturado', 'Valor': summary.totalAmount },
        { 'Concepto': 'Total ITBIS', 'Valor': summary.totalItbis },
        { 'Concepto': 'Total Retenciones', 'Valor': summary.totalRetention }
      ];
      
      const summaryHeaderRows: (string | number)[][] = [];

      summaryHeaderRows.push([companyName]);
      if (companyRnc) {
        summaryHeaderRows.push([`RNC: ${companyRnc}`]);
      }
      summaryHeaderRows.push(['Reporte 606 - Resumen']);
      summaryHeaderRows.push([`Período: ${selectedPeriod}`]);
      summaryHeaderRows.push([]);

      const summaryWs = XLSX.utils.json_to_sheet(summaryData as any, { origin: `A${summaryHeaderRows.length + 1}` } as any);
      XLSX.utils.sheet_add_aoa(summaryWs, summaryHeaderRows, { origin: 'A1' });
      summaryWs['!cols'] = [{ wch: 25 }, { wch: 20 }];
      XLSX.utils.book_append_sheet(wb, summaryWs, 'Resumen');
    }

    // Descargar archivo
    XLSX.writeFile(wb, `reporte_606_${selectedPeriod}.xlsx`, { cellStyles: true } as any);
  };

  const exportToTXT = () => {
    if (reportData.length === 0) return;

    let txtContent = `REPORTE 606 - COMPRAS Y SERVICIOS\n`;
    txtContent += `Período: ${selectedPeriod}\n`;
    txtContent += `Fecha de generación: ${formatDateEsDO(new Date())}\n\n`;

    if (summary) {
      txtContent += `RESUMEN:\n`;
      txtContent += `Total de registros: ${summary.totalRecords}\n`;
      txtContent += `Monto total: RD$ ${formatAmount(summary.totalAmount)}\n`;
      txtContent += `Total ITBIS: RD$ ${formatAmount(summary.totalItbis)}\n`;
      txtContent += `Total retenciones: RD$ ${formatAmount(summary.totalRetention)}\n\n`;
    }

    txtContent += `DETALLE:\n`;
    txtContent += `${'='.repeat(120)}\n`;

    reportData.forEach((row, index) => {
      txtContent += `${index + 1}. RNC/Cédula: ${row.rnc_cedula}\n`;
      txtContent += `   NCF: ${row.ncf}\n`;
      txtContent += `   Fecha: ${row.fecha_comprobante}\n`;
      txtContent += `   Monto: RD$ ${formatAmount(row.monto_facturado)}\n`;
      txtContent += `   ITBIS: RD$ ${formatAmount(row.itbis_facturado)}\n`;
      txtContent += `   Forma de pago: ${row.forma_pago}\n`;
      txtContent += `${'-'.repeat(80)}\n`;
    });

    const blob = new Blob([txtContent], { type: 'text/plain;charset=utf-8;' });
    const link = document.createElement('a');
    link.href = URL.createObjectURL(blob);
    link.download = `reporte_606_${selectedPeriod}.txt`;
    link.click();
  };

  const handleExportPdf = async () => {
    if (reportData.length === 0) return;

    try {
      const data = reportData.map(row => ({
        rnc_cedula: row.rnc_cedula,
        tipo_id: row.tipo_identificacion,
        tipo_bien_servicio: row.tipo_bienes_servicios,
        ncf: row.ncf,
        ncf_modificado: row.ncf_modificado || '',
        fecha_comprobante: row.fecha_comprobante,
        fecha_pago: row.fecha_pago,
        servicios_facturados: row.servicios_facturados,
        bienes_facturados: row.bienes_facturados,
        monto_facturado: row.monto_facturado,
        itbis_facturado: row.itbis_facturado,
        itbis_retenido: row.itbis_retenido,
        retencion_renta: row.retencion_renta,
        forma_pago: row.forma_pago,
      }));

      const columns = [
        { key: 'rnc_cedula', label: 'RNC/Cédula' },
        { key: 'tipo_id', label: 'Tipo ID' },
        { key: 'tipo_bien_servicio', label: 'Tipo Bien/Serv.' },
        { key: 'ncf', label: 'NCF' },
        { key: 'ncf_modificado', label: 'NCF Mod.' },
        { key: 'fecha_comprobante', label: 'F. Comp.' },
        { key: 'fecha_pago', label: 'F. Pago' },
        { key: 'servicios_facturados', label: 'Serv. Fact.' },
        { key: 'bienes_facturados', label: 'Bienes Fact.' },
        { key: 'monto_facturado', label: 'Monto Fact.' },
        { key: 'itbis_facturado', label: 'ITBIS Fact.' },
        { key: 'itbis_retenido', label: 'ITBIS Ret.' },
        { key: 'retencion_renta', label: 'Ret. Renta' },
        { key: 'forma_pago', label: 'Forma Pago' },
      ];

      await exportToPdf(
        data,
        columns,
        `reporte_606_${selectedPeriod}`,
        'Reporte 606 - Compras y Servicios',
        'l',
      );
    } catch (error) {
      console.error('Error exporting Reporte 606 to PDF:', error);
      alert('Error al exportar a PDF. Revisa la consola para más detalles.');
    }
  };

  return (
    <DashboardLayout>
      <div className="p-6">
        <div className="mb-6">
          <h1 className="text-2xl font-bold text-gray-900 mb-2">Reporte 606</h1>
          <p className="text-gray-600">Reporte de Compras y Servicios</p>
        </div>

        {/* Filtros */}
        <div className="bg-white rounded-lg shadow-sm border border-gray-200 p-6 mb-6">
          <h2 className="text-lg font-semibold text-gray-900 mb-4">Generar Reporte</h2>

          <div className="grid grid-cols-1 md:grid-cols-3 gap-4 mb-6">
            <div>
              <label className="block text-sm font-medium text-gray-700 mb-2">
                Año
              </label>
              <select
                value={selectedYear}
                onChange={(e) => setSelectedYear(e.target.value)}
                className="w-full px-3 py-2 border border-gray-300 rounded-md focus:outline-none focus:ring-2 focus:ring-blue-500"
              >
                {years.map((year) => (
                  <option key={year} value={year}>
                    {year}
                  </option>
                ))}
              </select>
            </div>

            <div>
              <label className="block text-sm font-medium text-gray-700 mb-2">
                Mes
              </label>
              <select
                value={selectedMonth}
                onChange={(e) => setSelectedMonth(e.target.value)}
                className="w-full px-3 py-2 border border-gray-300 rounded-md focus:outline-none focus:ring-2 focus:ring-blue-500"
              >
                {months.map((m) => (
                  <option key={m.value} value={m.value}>
                    {m.label}
                  </option>
                ))}
              </select>
            </div>
          </div>

          <button
            onClick={generateReport}
            disabled={loading || !selectedYear || !selectedMonth}
            className="bg-blue-600 text-white px-6 py-2 rounded-md hover:bg-blue-700 disabled:bg-gray-400 disabled:cursor-not-allowed whitespace-nowrap"
          >
            {loading ? 'Generando...' : 'Generar Reporte'}
          </button>
        </div>

        {/* Resultados */}
        {showResults && (
          <>
            {/* Resumen */}
            {summary && (
              <div className="bg-white rounded-lg shadow-sm border border-gray-200 p-6 mb-6">
                <h2 className="text-lg font-semibold text-gray-900 mb-4">Resumen del Período</h2>
                
                <div className="grid grid-cols-1 md:grid-cols-4 gap-4">
                  <div className="bg-blue-50 p-4 rounded-lg">
                    <div className="text-2xl font-bold text-blue-600">{summary.totalRecords}</div>
                    <div className="text-sm text-gray-600">Total Registros</div>
                  </div>
                  
                  <div className="bg-green-50 p-4 rounded-lg">
                    <div className="text-2xl font-bold text-green-600">
                      RD$ {formatAmount(summary.totalAmount)}
                    </div>
                    <div className="text-sm text-gray-600">Monto Total</div>
                  </div>
                  
                  <div className="bg-yellow-50 p-4 rounded-lg">
                    <div className="text-2xl font-bold text-yellow-600">
                      RD$ {formatAmount(summary.totalItbis)}
                    </div>
                    <div className="text-sm text-gray-600">Total ITBIS</div>
                  </div>
                  
                  <div className="bg-red-50 p-4 rounded-lg">
                    <div className="text-2xl font-bold text-red-600">
                      RD$ {formatAmount(summary.totalRetention)}
                    </div>
                    <div className="text-sm text-gray-600">Total Retenciones</div>
                  </div>
                </div>
              </div>
            )}

            {/* Botones de exportación */}
            <div className="bg-white rounded-lg shadow-sm border border-gray-200 p-6 mb-6">
              <h2 className="text-lg font-semibold text-gray-900 mb-4">Exportar Datos</h2>
              
              <div className="flex flex-wrap gap-3">
                <button
                  onClick={exportToCSV}
                  className="bg-green-600 text-white px-4 py-2 rounded-md hover:bg-green-700 flex items-center gap-2 whitespace-nowrap"
                >
                  <i className="ri-file-excel-2-line"></i>
                  Exportar CSV
                </button>
                
                <button
                  onClick={exportToExcel}
                  className="bg-blue-600 text-white px-4 py-2 rounded-md hover:bg-blue-700 flex items-center gap-2 whitespace-nowrap"
                >
                  <i className="ri-file-excel-line"></i>
                  Exportar Excel
                </button>
                
                <button
                  onClick={exportToTXT}
                  className="bg-gray-600 text-white px-4 py-2 rounded-md hover:bg-gray-700 flex items-center gap-2 whitespace-nowrap"
                >
                  <i className="ri-file-text-line"></i>
                  Exportar TXT
                </button>
                
                <button
                  onClick={handleExportPdf}
                  className="bg-red-600 text-white px-4 py-2 rounded-md hover:bg-red-700 flex items-center gap-2 whitespace-nowrap"
                >
                  <i className="ri-file-pdf-line"></i>
                  Exportar PDF
                </button>
              </div>
            </div>

            {/* Tabla de datos */}
            <div className="bg-white rounded-lg shadow-sm border border-gray-200">
              <div className="p-6 border-b border-gray-200">
                <h2 className="text-lg font-semibold text-gray-900">
                  Datos del Reporte ({reportData.length} registros)
                </h2>
              </div>
              
              <div className="overflow-x-auto">
                <table className="min-w-full divide-y divide-gray-200">
                  <thead className="bg-gray-50">
                    <tr>
                      <th className="px-6 py-3 text-left text-xs font-medium text-gray-500 uppercase tracking-wider">
                        RNC/Cédula
                      </th>
                      <th className="px-6 py-3 text-left text-xs font-medium text-gray-500 uppercase tracking-wider">
                        NCF
                      </th>
                      <th className="px-6 py-3 text-left text-xs font-medium text-gray-500 uppercase tracking-wider">
                        Fecha
                      </th>
                      <th className="px-6 py-3 text-left text-xs font-medium text-gray-500 uppercase tracking-wider">
                        Monto
                      </th>
                      <th className="px-6 py-3 text-left text-xs font-medium text-gray-500 uppercase tracking-wider">
                        ITBIS
                      </th>
                      <th className="px-6 py-3 text-left text-xs font-medium text-gray-500 uppercase tracking-wider">
                        Retención
                      </th>
                      <th className="px-6 py-3 text-left text-xs font-medium text-gray-500 uppercase tracking-wider">
                        Forma Pago
                      </th>
                    </tr>
                  </thead>
                  <tbody className="bg-white divide-y divide-gray-200">
                    {reportData.map((row) => (
                      <tr key={row.id} className="hover:bg-gray-50">
                        <td className="px-6 py-4 whitespace-nowrap text-sm text-gray-900">
                          {row.rnc_cedula}
                        </td>
                        <td className="px-6 py-4 whitespace-nowrap text-sm text-gray-900">
                          {row.ncf}
                        </td>
                        <td className="px-6 py-4 whitespace-nowrap text-sm text-gray-900">
                          {row.fecha_comprobante}
                        </td>
                        <td className="px-6 py-4 whitespace-nowrap text-sm text-gray-900">
                          RD$ {formatAmount(row.monto_facturado)}
                        </td>
                        <td className="px-6 py-4 whitespace-nowrap text-sm text-gray-900">
                          RD$ {formatAmount(row.itbis_facturado)}
                        </td>
                        <td className="px-6 py-4 whitespace-nowrap text-sm text-gray-900">
                          RD$ {formatAmount(row.itbis_retenido)}
                        </td>
                        <td className="px-6 py-4 whitespace-nowrap text-sm text-gray-900">
                          {row.forma_pago}
                        </td>
                      </tr>
                    ))}
                  </tbody>
                </table>
              </div>
            </div>
          </>
        )}
      </div>
    </DashboardLayout>
  );
}