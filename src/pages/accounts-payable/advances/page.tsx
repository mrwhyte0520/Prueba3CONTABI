import { useEffect, useState } from 'react';
import DashboardLayout from '../../../components/layout/DashboardLayout';
import { useAuth } from '../../../hooks/useAuth';
import { apSupplierAdvancesService, suppliersService } from '../../../services/database';

interface SupplierAdvance {
  id: string;
  number: string;
  date: string;
  supplierId: string;
  supplierName: string;
  amount: number;
  reason: string;
  status: 'Pendiente' | 'Aprobado' | 'Aplicado' | 'Rechazado';
  dueDate: string | null;
  remainingBalance: number;
  appliedAmount: number;
}

export default function AdvancesPage() {
  const { user } = useAuth();

  const [showModal, setShowModal] = useState(false);
  const [editingAdvance, setEditingAdvance] = useState<SupplierAdvance | null>(null);
  const [filterStatus, setFilterStatus] = useState('all');

  const [advances, setAdvances] = useState<SupplierAdvance[]>([]);
  const [suppliers, setSuppliers] = useState<Array<{ id: string; name: string }>>([]);

  const [formData, setFormData] = useState({
    supplierId: '',
    amount: '',
    reason: '',
    dueDate: '',
  });

  const loadSuppliers = async () => {
    if (!user?.id) {
      setSuppliers([]);
      return;
    }
    try {
      const rows = await suppliersService.getAll(user.id);
      setSuppliers((rows || []).map((s: any) => ({ id: String(s.id), name: s.name || '' })));
    } catch (error) {
      // eslint-disable-next-line no-console
      console.error('Error cargando proveedores para anticipos CxP', error);
      setSuppliers([]);
    }
  };

  const mapDbStatusToUi = (status: string | null | undefined): SupplierAdvance['status'] => {
    switch (status) {
      case 'approved':
        return 'Aprobado';
      case 'applied':
        return 'Aplicado';
      case 'cancelled':
        return 'Rechazado';
      default:
        return 'Pendiente';
    }
  };

  const mapUiStatusToDb = (status: SupplierAdvance['status']): string => {
    switch (status) {
      case 'Aprobado':
        return 'approved';
      case 'Aplicado':
        return 'applied';
      case 'Rechazado':
        return 'cancelled';
      default:
        return 'pending';
    }
  };

  const loadAdvances = async () => {
    if (!user?.id) {
      setAdvances([]);
      return;
    }
    try {
      const rows = await apSupplierAdvancesService.getAll(user.id);
      const mapped: SupplierAdvance[] = (rows || []).map((a: any) => {
        const amount = Number(a.amount) || 0;
        const applied = Number(a.applied_amount) || 0;
        const balance = Number(a.balance_amount) || (amount - applied);
        return {
          id: String(a.id),
          number: a.advance_number || '',
          date: a.advance_date || (a.created_at ? String(a.created_at).slice(0, 10) : ''),
          supplierId: String(a.supplier_id),
          supplierName: (a.suppliers as any)?.name || 'Proveedor',
          amount,
          reason: a.description || '',
          status: mapDbStatusToUi(a.status),
          dueDate: a.due_date || null,
          remainingBalance: balance,
          appliedAmount: applied,
        };
      });
      setAdvances(mapped);
    } catch (error) {
      // eslint-disable-next-line no-console
      console.error('Error cargando anticipos a suplidores', error);
      setAdvances([]);
    }
  };

  useEffect(() => {
    loadSuppliers();
    loadAdvances();
    // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [user?.id]);

  const filteredAdvances = advances.filter((advance) => {
    return filterStatus === 'all' || advance.status === filterStatus;
  });

  const handleSubmit = async (e: React.FormEvent) => {
    e.preventDefault();

    if (!user?.id) {
      alert('Debes iniciar sesión para registrar anticipos');
      return;
    }

    const amountNumber = Number(formData.amount);
    if (!formData.supplierId || !formData.reason.trim() || isNaN(amountNumber) || amountNumber <= 0) {
      alert('Proveedor, motivo y monto válido son obligatorios');
      return;
    }

    const today = new Date().toISOString().split('T')[0];
    const advanceNumber = editingAdvance?.number || `ADV-${new Date().getFullYear()}-${String(advances.length + 1).padStart(3, '0')}`;

    try {
      if (editingAdvance) {
        const dbStatus = mapUiStatusToDb(editingAdvance.status);
        const appliedAmount = editingAdvance.appliedAmount;
        const balanceAmount = amountNumber - appliedAmount;

        await apSupplierAdvancesService.updateStatus(editingAdvance.id, dbStatus, {
          appliedAmount,
          balanceAmount,
        });

        // Nota: no tenemos un método update completo en el servicio, así que solo actualizamos montos/estatus.
        // Para cambios más profundos se podría ampliar el servicio con un método update similar a create.
      } else {
        await apSupplierAdvancesService.create(user.id, {
          supplier_id: formData.supplierId,
          advance_number: advanceNumber,
          advance_date: today,
          amount: amountNumber,
          reference: null,
          description: formData.reason,
          status: 'pending',
          applied_amount: 0,
          balance_amount: amountNumber,
        });
      }

      await loadAdvances();
      resetForm();
      alert(editingAdvance ? 'Anticipo actualizado exitosamente' : 'Anticipo creado exitosamente');
    } catch (error: any) {
      // eslint-disable-next-line no-console
      console.error('Error guardando anticipo de suplidor', error);
      alert(error?.message || 'Error al guardar el anticipo');
    }
  };

  const resetForm = () => {
    setFormData({
      supplierId: '',
      amount: '',
      reason: '',
      dueDate: '',
    });
    setEditingAdvance(null);
    setShowModal(false);
  };

  const handleEdit = (advance: SupplierAdvance) => {
    setEditingAdvance(advance);
    setFormData({
      supplierId: advance.supplierId,
      amount: advance.amount.toString(),
      reason: advance.reason,
      dueDate: advance.dueDate || '',
    });
    setShowModal(true);
  };

  const handleApprove = async (id: string) => {
    const advance = advances.find(a => a.id === id);
    if (!advance) return;
    if (!confirm('¿Aprobar este anticipo?')) return;
    try {
      await apSupplierAdvancesService.updateStatus(id, 'approved', {
        appliedAmount: advance.appliedAmount,
        balanceAmount: advance.remainingBalance,
      });
      await loadAdvances();
      alert('Anticipo aprobado exitosamente');
    } catch (error) {
      // eslint-disable-next-line no-console
      console.error('Error aprobando anticipo de suplidor', error);
      alert('No se pudo aprobar el anticipo');
    }
  };

  const handleReject = async (id: string) => {
    const advance = advances.find(a => a.id === id);
    if (!advance) return;
    if (!confirm('¿Rechazar este anticipo?')) return;
    try {
      await apSupplierAdvancesService.updateStatus(id, 'cancelled', {
        appliedAmount: advance.appliedAmount,
        balanceAmount: advance.remainingBalance,
      });
      await loadAdvances();
      alert('Anticipo rechazado');
    } catch (error) {
      // eslint-disable-next-line no-console
      console.error('Error rechazando anticipo de suplidor', error);
      alert('No se pudo rechazar el anticipo');
    }
  };

  const handleApply = async (id: string) => {
    const advance = advances.find(a => a.id === id);
    if (!advance) return;
    if (!confirm(`¿Aplicar anticipo de RD$ ${advance.remainingBalance.toLocaleString()}?`)) return;
    try {
      const newApplied = advance.appliedAmount + advance.remainingBalance;
      const newBalance = 0;
      await apSupplierAdvancesService.updateStatus(id, 'applied', {
        appliedAmount: newApplied,
        balanceAmount: newBalance,
      });
      await loadAdvances();
      alert('Anticipo aplicado exitosamente');
    } catch (error) {
      // eslint-disable-next-line no-console
      console.error('Error aplicando anticipo de suplidor', error);
      alert('No se pudo aplicar el anticipo');
    }
  };

  const exportToExcel = () => {
    alert('Exportando anticipos a Excel... (pendiente de implementar integración real)');
  };

  const printAdvance = (advance: SupplierAdvance) => {
    alert(`Imprimiendo anticipo: ${advance.number}`);
  };

  return (
    <DashboardLayout>
      <div className="space-y-6">
        {/* Header */}
        <div className="flex items-center justify-between">
          <div>
            <h1 className="text-2xl font-bold text-gray-900">Anticipos a Proveedores</h1>
            <p className="text-gray-600">Gestiona anticipos y pagos adelantados</p>
          </div>
          <div className="flex space-x-3">
            <button 
              onClick={exportToExcel}
              className="bg-green-600 text-white px-4 py-2 rounded-lg hover:bg-green-700 transition-colors whitespace-nowrap"
            >
              <i className="ri-file-excel-line mr-2"></i>
              Exportar Excel
            </button>
            <button 
              onClick={() => setShowModal(true)}
              className="bg-blue-600 text-white px-4 py-2 rounded-lg hover:bg-blue-700 transition-colors whitespace-nowrap"
            >
              <i className="ri-add-line mr-2"></i>
              Nuevo Anticipo
            </button>
          </div>
        </div>

        {/* Stats Cards */}
        <div className="grid grid-cols-1 md:grid-cols-4 gap-6">
          <div className="bg-white rounded-lg shadow-sm border border-gray-200 p-6">
            <div className="flex items-center">
              <div className="w-12 h-12 bg-blue-100 rounded-lg flex items-center justify-center mr-4">
                <i className="ri-money-dollar-circle-line text-xl text-blue-600"></i>
              </div>
              <div>
                <p className="text-sm font-medium text-gray-600">Total Anticipos</p>
                <p className="text-2xl font-bold text-gray-900">RD$ {advances.reduce((sum, a) => sum + a.amount, 0).toLocaleString()}</p>
              </div>
            </div>
          </div>
          <div className="bg-white rounded-lg shadow-sm border border-gray-200 p-6">
            <div className="flex items-center">
              <div className="w-12 h-12 bg-orange-100 rounded-lg flex items-center justify-center mr-4">
                <i className="ri-time-line text-xl text-orange-600"></i>
              </div>
              <div>
                <p className="text-sm font-medium text-gray-600">Pendientes</p>
                <p className="text-2xl font-bold text-gray-900">RD$ {advances.filter(a => a.status === 'Pendiente').reduce((sum, a) => sum + a.amount, 0).toLocaleString()}</p>
              </div>
            </div>
          </div>
          <div className="bg-white rounded-lg shadow-sm border border-gray-200 p-6">
            <div className="flex items-center">
              <div className="w-12 h-12 bg-green-100 rounded-lg flex items-center justify-center mr-4">
                <i className="ri-check-line text-xl text-green-600"></i>
              </div>
              <div>
                <p className="text-sm font-medium text-gray-600">Aprobados</p>
                <p className="text-2xl font-bold text-gray-900">RD$ {advances.filter(a => a.status === 'Aprobado').reduce((sum, a) => sum + a.amount, 0).toLocaleString()}</p>
              </div>
            </div>
          </div>
          <div className="bg-white rounded-lg shadow-sm border border-gray-200 p-6">
            <div className="flex items-center">
              <div className="w-12 h-12 bg-purple-100 rounded-lg flex items-center justify-center mr-4">
                <i className="ri-wallet-line text-xl text-purple-600"></i>
              </div>
              <div>
                <p className="text-sm font-medium text-gray-600">Saldo Pendiente</p>
                <p className="text-2xl font-bold text-gray-900">RD$ {advances.reduce((sum, a) => sum + a.remainingBalance, 0).toLocaleString()}</p>
              </div>
            </div>
          </div>
        </div>

        {/* Filters */}
        <div className="bg-white rounded-lg shadow-sm border border-gray-200 p-6">
          <div className="grid grid-cols-1 md:grid-cols-3 gap-4">
            <div>
              <label className="block text-sm font-medium text-gray-700 mb-2">Estado</label>
              <select 
                value={filterStatus}
                onChange={(e) => setFilterStatus(e.target.value)}
                className="w-full px-3 py-2 border border-gray-300 rounded-lg focus:ring-2 focus:ring-blue-500 focus:border-blue-500"
              >
                <option value="all">Todos los Estados</option>
                <option value="Pendiente">Pendiente</option>
                <option value="Aprobado">Aprobado</option>
                <option value="Aplicado">Aplicado</option>
                <option value="Rechazado">Rechazado</option>
              </select>
            </div>
            <div className="md:col-span-2 flex items-end">
              <button 
                onClick={() => setFilterStatus('all')}
                className="w-full bg-gray-600 text-white py-2 px-4 rounded-lg hover:bg-gray-700 transition-colors whitespace-nowrap"
              >
                Limpiar Filtros
              </button>
            </div>
          </div>
        </div>

        {/* Advances Table */}
        <div className="bg-white rounded-lg shadow-sm border border-gray-200">
          <div className="p-6 border-b border-gray-200">
            <h3 className="text-lg font-semibold text-gray-900">Lista de Anticipos</h3>
          </div>
          <div className="overflow-x-auto">
            <table className="min-w-full divide-y divide-gray-200">
              <thead className="bg-gray-50">
                <tr>
                  <th className="px-6 py-3 text-left text-xs font-medium text-gray-500 uppercase tracking-wider">Número</th>
                  <th className="px-6 py-3 text-left text-xs font-medium text-gray-500 uppercase tracking-wider">Fecha</th>
                  <th className="px-6 py-3 text-left text-xs font-medium text-gray-500 uppercase tracking-wider">Proveedor</th>
                  <th className="px-6 py-3 text-right text-xs font-medium text-gray-500 uppercase tracking-wider">Monto</th>
                  <th className="px-6 py-3 text-right text-xs font-medium text-gray-500 uppercase tracking-wider">Saldo</th>
                  <th className="px-6 py-3 text-left text-xs font-medium text-gray-500 uppercase tracking-wider">Vencimiento</th>
                  <th className="px-6 py-3 text-left text-xs font-medium text-gray-500 uppercase tracking-wider">Estado</th>
                  <th className="px-6 py-3 text-left text-xs font-medium text-gray-500 uppercase tracking-wider">Acciones</th>
                </tr>
              </thead>
              <tbody className="bg-white divide-y divide-gray-200">
                {filteredAdvances.map((advance) => (
                  <tr key={advance.id} className="hover:bg-gray-50">
                    <td className="px-6 py-4 whitespace-nowrap text-sm font-medium text-gray-900">{advance.number}</td>
                    <td className="px-6 py-4 whitespace-nowrap text-sm text-gray-900">{advance.date}</td>
                    <td className="px-6 py-4 whitespace-nowrap">
                      <div>
                        <div className="text-sm font-medium text-gray-900">{advance.supplierName}</div>
                        <div className="text-sm text-gray-500 truncate max-w-xs">{advance.reason}</div>
                      </div>
                    </td>
                    <td className="px-6 py-4 whitespace-nowrap text-sm text-right font-semibold text-gray-900">
                      RD$ {advance.amount.toLocaleString()}
                    </td>
                    <td className="px-6 py-4 whitespace-nowrap text-sm text-right font-semibold text-gray-900">
                      RD$ {advance.remainingBalance.toLocaleString()}
                    </td>
                    <td className="px-6 py-4 whitespace-nowrap text-sm text-gray-900">{advance.dueDate}</td>
                    <td className="px-6 py-4 whitespace-nowrap">
                      <span className={`inline-flex px-2 py-1 text-xs font-medium rounded-full ${
                        advance.status === 'Aprobado' ? 'bg-green-100 text-green-800' :
                        advance.status === 'Pendiente' ? 'bg-orange-100 text-orange-800' :
                        advance.status === 'Aplicado' ? 'bg-blue-100 text-blue-800' :
                        'bg-red-100 text-red-800'
                      }`}>
                        {advance.status}
                      </span>
                    </td>
                    <td className="px-6 py-4 whitespace-nowrap text-sm font-medium">
                      <div className="flex space-x-2">
                        <button 
                          onClick={() => printAdvance(advance)}
                          className="text-gray-600 hover:text-gray-900 whitespace-nowrap"
                        >
                          <i className="ri-printer-line"></i>
                        </button>
                        <button 
                          onClick={() => handleEdit(advance)}
                          className="text-indigo-600 hover:text-indigo-900 whitespace-nowrap"
                        >
                          <i className="ri-edit-line"></i>
                        </button>
                        {advance.status === 'Pendiente' && (
                          <>
                            <button 
                              onClick={() => handleApprove(advance.id)}
                              className="text-green-600 hover:text-green-900 whitespace-nowrap"
                            >
                              <i className="ri-check-line"></i>
                            </button>
                            <button 
                              onClick={() => handleReject(advance.id)}
                              className="text-red-600 hover:text-red-900 whitespace-nowrap"
                            >
                              <i className="ri-close-line"></i>
                            </button>
                          </>
                        )}
                        {advance.status === 'Aprobado' && advance.remainingBalance > 0 && (
                          <button 
                            onClick={() => handleApply(advance.id)}
                            className="text-blue-600 hover:text-blue-900 whitespace-nowrap"
                          >
                            <i className="ri-wallet-line"></i>
                          </button>
                        )}
                      </div>
                    </td>
                  </tr>
                ))}
              </tbody>
            </table>
          </div>
        </div>

        {/* Advance Modal */}
        {showModal && (
          <div className="fixed inset-0 bg-black bg-opacity-50 flex items-center justify-center z-50">
            <div className="bg-white rounded-lg shadow-xl max-w-2xl w-full mx-4 max-h-[90vh] overflow-y-auto">
              <div className="p-6 border-b border-gray-200">
                <h3 className="text-lg font-semibold text-gray-900">
                  {editingAdvance ? 'Editar Anticipo' : 'Nuevo Anticipo'}
                </h3>
              </div>
              <form onSubmit={handleSubmit} className="p-6 space-y-4">
                <div className="grid grid-cols-1 md:grid-cols-2 gap-4">
                  <div>
                    <label className="block text-sm font-medium text-gray-700 mb-2">Proveedor *</label>
                    <select 
                      required
                      value={formData.supplierId}
                      onChange={(e) => setFormData({ ...formData, supplierId: e.target.value })}
                      className="w-full px-3 py-2 border border-gray-300 rounded-lg focus:ring-2 focus:ring-blue-500 focus:border-blue-500"
                    >
                      <option value="">Seleccionar proveedor</option>
                      {suppliers.map((supplier) => (
                        <option key={supplier.id} value={supplier.id}>{supplier.name}</option>
                      ))}
                    </select>
                  </div>
                  <div>
                    <label className="block text-sm font-medium text-gray-700 mb-2">Monto *</label>
                    <input 
                      type="number"
                      required
                      step="0.01"
                      value={formData.amount}
                      onChange={(e) => setFormData({...formData, amount: e.target.value})}
                      className="w-full px-3 py-2 border border-gray-300 rounded-lg focus:ring-2 focus:ring-blue-500 focus:border-blue-500"
                      placeholder="0.00"
                    />
                  </div>
                  <div className="md:col-span-2">
                    <label className="block text-sm font-medium text-gray-700 mb-2">Motivo *</label>
                    <textarea 
                      required
                      value={formData.reason}
                      onChange={(e) => setFormData({...formData, reason: e.target.value})}
                      rows={3}
                      className="w-full px-3 py-2 border border-gray-300 rounded-lg focus:ring-2 focus:ring-blue-500 focus:border-blue-500"
                      placeholder="Describe el motivo del anticipo..."
                    />
                  </div>
                  <div>
                    <label className="block text-sm font-medium text-gray-700 mb-2">Fecha de Vencimiento *</label>
                    <input 
                      type="date"
                      required
                      value={formData.dueDate}
                      onChange={(e) => setFormData({...formData, dueDate: e.target.value})}
                      className="w-full px-3 py-2 border border-gray-300 rounded-lg focus:ring-2 focus:ring-blue-500 focus:border-blue-500"
                    />
                  </div>
                </div>
                <div className="flex justify-end space-x-3 pt-4">
                  <button 
                    type="button"
                    onClick={resetForm}
                    className="px-4 py-2 border border-gray-300 rounded-lg text-gray-700 hover:bg-gray-50 whitespace-nowrap"
                  >
                    Cancelar
                  </button>
                  <button 
                    type="submit"
                    className="px-4 py-2 bg-blue-600 text-white rounded-lg hover:bg-blue-700 whitespace-nowrap"
                  >
                    {editingAdvance ? 'Actualizar' : 'Crear'} Anticipo
                  </button>
                </div>
              </form>
            </div>
          </div>
        )}
      </div>
    </DashboardLayout>
  );
}